import { mkdir, readdir, readFile, writeFile } from "node:fs/promises";
import path from "node:path";

const repoRoot = process.cwd();
const protocolRoot = process.env.SCURO_PROTOCOL_ROOT
  ? path.resolve(process.env.SCURO_PROTOCOL_ROOT)
  : path.resolve(repoRoot, "../scuro");
const docsGeneratedRoot = path.join(protocolRoot, "docs", "generated");
const contractsRoot = path.join(docsGeneratedRoot, "contracts");
const outputRoot = path.join(repoRoot, "src", "generated");

const jsonHeader = "// This file is generated by scripts/generate-protocol.mjs. Do not edit manually.\n\n";

function serializeConst(name, value) {
  return `export const ${name} = ${JSON.stringify(value, null, 2)} as const;\n`;
}

function toIdentifier(name) {
  return name
    .replace(/[^a-zA-Z0-9]+(.)/g, (_, chr) => chr.toUpperCase())
    .replace(/^[^a-zA-Z_]+/, "")
    .replace(/^[A-Z]/, (chr) => chr.toLowerCase());
}

async function readJson(filePath) {
  return JSON.parse(await readFile(filePath, "utf8"));
}

async function main() {
  await mkdir(outputRoot, { recursive: true });

  const protocolManifest = await readJson(path.join(docsGeneratedRoot, "protocol-manifest.json"));
  const enumLabels = await readJson(path.join(docsGeneratedRoot, "enum-labels.json"));
  const eventSignatures = await readJson(path.join(docsGeneratedRoot, "event-signatures.json"));
  const proofInputs = await readJson(path.join(docsGeneratedRoot, "proof-inputs.json"));

  const abiFiles = (await readdir(contractsRoot))
    .filter((file) => file.endsWith(".abi.json"))
    .sort();

  const abis = {};
  for (const file of abiFiles) {
    const contractName = file.replace(/\.abi\.json$/, "");
    abis[contractName] = await readJson(path.join(contractsRoot, file));
  }

  const contractNames = protocolManifest.contracts.map((contract) => contract.name);
  const groupedLabels = protocolManifest.deployment_output_labels;
  const deploymentOutputLabels = Object.values(groupedLabels).flat();
  const contractDeploymentLabels = [
    ...groupedLabels.core,
    ...groupedLabels.controllers,
    ...groupedLabels.engines,
    ...groupedLabels.verifiers
  ];

  const code = [
    jsonHeader,
    serializeConst("protocolManifest", protocolManifest),
    serializeConst("enumLabels", enumLabels),
    serializeConst("eventSignatures", eventSignatures),
    serializeConst("proofInputs", proofInputs),
    serializeConst("abis", abis),
    serializeConst("contractNames", contractNames),
    serializeConst("deploymentOutputLabelGroups", groupedLabels),
    serializeConst("deploymentOutputLabels", deploymentOutputLabels),
    serializeConst("contractDeploymentLabels", contractDeploymentLabels),
    "export type ProtocolManifest = typeof protocolManifest;\n",
    "export type ContractName = typeof contractNames[number];\n",
    "export type DeploymentOutputLabel = typeof deploymentOutputLabels[number];\n",
    "export type ContractDeploymentLabel = typeof contractDeploymentLabels[number];\n",
    "export type DeploymentOutputLabelGroups = typeof deploymentOutputLabelGroups;\n",
    "export type ProtocolAbiMap = typeof abis;\n",
    "export type EnumLabelsMap = typeof enumLabels;\n",
    "export type EventSignaturesMap = typeof eventSignatures;\n",
    "export type ProofInputsMap = typeof proofInputs;\n"
  ].join("");

  await writeFile(path.join(outputRoot, "protocol.ts"), code, "utf8");

  const aliasLines = [];
  for (const abiFile of abiFiles) {
    const contractName = abiFile.replace(/\.abi\.json$/, "");
    const exportName = `${toIdentifier(contractName)}Abi`;
    aliasLines.push(`export const ${exportName} = abis.${contractName};`);
  }

  await writeFile(
    path.join(outputRoot, "abis.ts"),
    `${jsonHeader}import { abis } from "./protocol";\n\n${aliasLines.join("\n")}\n`,
    "utf8"
  );

  await writeFile(
    path.join(outputRoot, "index.ts"),
    `${jsonHeader}export * from "./protocol";\nexport * from "./abis";\n`,
    "utf8"
  );
}

main().catch((error) => {
  console.error(error);
  process.exitCode = 1;
});
